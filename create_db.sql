  CREATE OR REPLACE EDITIONABLE PROCEDURE "C##ANNA"."WORKDAYS_IN_MONTH" (month_name IN varchar2, emp_id IN NUMBER, tmp OUT sys_refcursor)
as
begin
    open tmp for
        select work_calendar.id, to_char(work_calendar.day, 'dd'), day_types.title, marks.id, marks.title, marks.transcription
            from work_calendar
            left outer join (
                select * from employees_and_calendar_days
                where employees_and_calendar_days.employee_id = emp_id
            ) T
            on work_calendar.id = T.work_calendar_id
            left outer join day_types on work_calendar.id_day_type = day_types.id
            left outer join marks on T.mark_id = marks.id
            where to_char(work_calendar.day, 'Month YYYY') = month_name
            order by work_calendar.day;
end;

