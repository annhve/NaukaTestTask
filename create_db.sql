  function get_hash (p_username  IN  VARCHAR2,
                     p_password  IN  VARCHAR2)
    return VARCHAR2 as
    l_salt VARCHAR2(30) := 'SomeRandomSalt';
  begin
    return DBMS_CRYPTO.hash(UTL_RAW.CAST_TO_RAW(p_username || l_salt || p_password), DBMS_CRYPTO.HASH_SH1);
  end;

  procedure add_user (p_username  IN  VARCHAR2,
                      p_password  IN  VARCHAR2,
                      p_acc_type_id   IN  NUMBER,
                      p_id        OUT NUMBER) as
  begin
    insert into users (
      login,
      password,
      id_role
    )
    values (
      p_username,
      get_hash(p_username, p_password),
      p_acc_type_id
    )
    returning id into p_id;

    COMMIT;
  END;

  PROCEDURE valid_user (p_username  IN  VARCHAR2,
                        p_password  IN  VARCHAR2,
                        p_id       OUT NUMBER) AS
  BEGIN
    SELECT id
    INTO   p_id
    FROM   users
    WHERE  login = p_username
    AND    password = get_hash(p_username, p_password);
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      RAISE_APPLICATION_ERROR(-20007, 'Invalid username/password.');
  END;

END;

